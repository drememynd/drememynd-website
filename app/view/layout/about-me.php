<div class="content">
    What is good technical leadership?
</div>
<div class="content">
    Good technical leaders have good listening skills, 
    and take the time to understand competing objectives and balance the business problem, uncover the customer 
end-goal (not just the single request or the stated need), and set a course for fulfilling those goals in a product vision.
</div>
<div class="content">
    Good technical leaders review new technologies and determine whether or not they meet strategic product
and development objectives, including how the technology will integrate, how it will scale and performs, and whether it meets financial and 
product goals. It's important to collaborate with both business and development leadership to identify a technical vision and drive that forward.
</div>
<div class="content">
     Good technical leaders are
cross-functional collaborators who negotiate at a strategic level, providing leadership by developing technical solutions, without letting
their egos block progress or prevent them from changing directions when needed.  They take collaborative ownership over the health and future of the product.
</div>
<div class="content">
    Good technical leaders are strategic, understanding how to balance the demand of system design and product management. It is important to set a course for long-term 
improvement and not quickly settle for increasing technical debt for short-term fixes.  This can be done without compromising on customer deliverables.
</div>