<div class="content">
    <strong>What is good technical leadership?</strong>
</div>
<div class="content">
    <strong>Good technical leaders have good listening skills, </strong>
    and take the time to understand competing objectives and balance the business problem, uncover the customer 
end-goal (not just the single request or the stated need), and set a course for fulfilling those goals in a product vision.
</div>
<div class="content">
    <strong>Good technical leaders are strategic.</strong> They understand how to balance the demand of system design and product management. It is important to set a course for long-term 
improvement and not quickly settle for increasing technical debt for short-term fixes.  This can be done without compromising on customer deliverables.
</div>
<div class="content">
    <strong>Good technical leaders are cross-functional collaborators.</strong> They negotiate at a strategic level, with diverse stakeholders throughout the organization, providing leadership by developing technical solutions, without letting
their egos block progress or prevent them from changing directions when needed.  They take collaborative ownership over the health and future of the product.
</div>
<div class="content">
    <strong>Good technical leaders are technical.</strong>  They have a strong background working hands on with technology, bringing that expertise to their work.  
    It's important to collaborate with both business and development leadership to identify a technical vision and drive that forward.
    It's also important to stay in touch with the technologies the system is built with now, and might be built with in the future.
</div>